package JavaBasics;
public class DataTypes {

	// main method() --> starting/execution point of the program
	public static void main(String[] args) {

	//primitive data types: int, double, char, boolean
	// 1.int:
	int i = 10;
	System.out.println(i);

	// 2.double
	double j = 12.34;
	double j1 = 23.35;
	double j2 = 100; //100.00

	// 3.char: only single digit value. should be written within single quotes
	char c = 'A';
	char d = '$';
	char e = '/';
	char f = '@';
	char g = 's';
	char h = '+';

	// 4.boolean
	boolean k = true;
	boolean l = false;

	// 5.String: is a class. Not a datatype
	String s = "Vignesh";
	String t = "100";
	String u = "12.34";
	String v = "a";
	String w = "@";
	String x = "true";
	}
}